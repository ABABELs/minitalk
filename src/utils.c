void	recep_len(int *bit_len, int sig, int *u, char *str)
{
	if (sig == SIGUSR2)
		*u = *u | (0x0001 << *bit_len);
	if (*bit_len == 32)
	{
		str = malloc(sizeof(char) * *u);
		str[*u - 1] = 0;
		(*bit_len)++;
	}
}